    useCORS: true
    function printInvoice() {
        const invoice = document.getElementById("invoice");
        const { jsPDF } = window.jspdf;
        const pdf = new jsPDF("p", "pt", "a4", true); 

        const options = {
            margin: 0, // Use numeric value for margin
            html2canvas: {
                scale: 0.5, // Adjust the scale value as needed
                logging: true, // Enable logging for troubleshooting
            }
        };

        // Get the header content from the HTML element with id "headerinvoice"
        const header = document.getElementById("headerofinvoice").innerHTML;

        // Add header to the PDF
        pdf.setFontSize(18);
        pdf.text(header, 20, 20);

        pdf.html(invoice, options).then(() => {
            pdf.save("invoice.pdf");
        });
    }
